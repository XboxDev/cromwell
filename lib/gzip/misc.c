int decompress_kernel(char *out, char *data, int len)
{
	input_data = data;
	input_len = len;
	
	output_data = out;

	free_mem_ptr = 0x01000000;
	free_mem_end_ptr = 0x02000000;

	makecrc();
	gunzip();
	return 1;
}
